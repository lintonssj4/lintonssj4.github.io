Bandeira significados:
- Cor amarela: é mais comumente associada ao liberalismo social e o libertarianismo
- Cor branca: Simboliza a paz porque no final de tudo, priorizamos a paz e a organização entre a sociedade
- O pássaro em voô: Representa a liberdade, pois "Voar pelo céu" é um dos maiores simbolismo de liberdade e o avanço contínuo para um país melhor
- Cor Azul: Confiança, Lealdade e Estabilidade. O nosso foco é passar confiança e lealdade para o nosso povo.